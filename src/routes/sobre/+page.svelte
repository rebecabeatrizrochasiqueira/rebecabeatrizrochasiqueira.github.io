  <p>O arco-íris é um fenômeno óptico e meteorológico que ocorre quando a luz do sol é refratada, refletida e dispersa em gotículas de água, criando um espectro de luz no céu. Ele geralmente aparece em um arco circular, exibindo as cores do espectro visível na seguinte ordem: vermelho, laranja, amarelo, verde, azul, anil e violeta.</p>
  <p>O arco-íris é composto por sete cores, cada uma com seus significados e representações únicas, refletindo a diversidade e a beleza do mundo natural.</p>
  <ul>
      <li style="color: red;"><strong>Vermelho</strong>: A cor mais intensa do arco-íris, simbolizando paixão, energia e excitação.</li>
      <li style="color: orange;"><strong>Laranja</strong>: Uma mistura vibrante de vermelho e amarelo, representando entusiasmo, criatividade e sucesso.</li>
      <li style="color: yellow;"><strong>Amarelo</strong>: Associada à luz, otimismo e felicidade. É a cor que chama a atenção e ilumina o espírito.</li>
      <li style="color: green;"><strong>Verde</strong>: Simboliza a natureza, crescimento e renovação. Traz uma sensação de calma e equilíbrio.</li>
      <li style="color: blue;"><strong>Azul</strong>: Cor do céu e do mar, refletindo tranquilidade, confiança e sabedoria.</li>
      <li style="color: indigo;"><strong>Anil</strong>: Tom profundo e misterioso de azul, evocando introspecção e intuição.</li>
      <li style="color: violet;"><strong>Violeta</strong>: Representa espiritualidade, imaginação e inspiração. Uma cor mágica e transformadora.</li>
  </ul>
  <h2 style="color: #ff6347; background-color: #0000ff; padding: 10px;">Significado de Ver um Arco-íris</h2>
  <p style="color: red;">Ver um arco-íris</p>
  <p style="color: orange;">é frequentemente considerado um símbolo</p>
  <p style="color: yellow;">de esperança, promessa</p>
  <p style="color: green;">e novos começos.</p>
  <p style="color: blue;">Muitas culturas acreditam</p>
  <p style="color: indigo;">que os arco-íris trazem sorte</p>
  <p style="color: violet;">e são sinais de que algo bom está por vir.</p>
  <h2 style="color: #ff6347; background-color: #0000ff; padding: 10px;">Lendas Sobre o Arco-íris</h2>
  <ul>
      <li style="color: red;">Na mitologia nórdica, o arco-íris é visto como uma ponte (Bifrost) que conecta Midgard (a Terra) a Asgard (o reino dos deuses).</li>
      <li style="color: orange;">Na cultura havaiana, acredita-se que os arco-íris são caminhos pelos quais os espíritos dos ancestrais viajam para alcançar o outro mundo.</li>
      <li style="color: yellow;">Em algumas lendas irlandesas, no fim do arco-íris há um pote de ouro guardado por um leprechaun, um ser mitológico. Estes tesouros simbolizam sorte e riqueza.</li>
  </ul>
